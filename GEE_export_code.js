var geo_bound_filter = ee.Filter.bounds(geo);

var genDWImage = function (start_date, end_date) {
    var date_filter = ee.Filter.date(start_date, end_date);
    var col_filter = ee.Filter.and(geo_bound_filter, date_filter);

    var DWcol = ee.ImageCollection('GOOGLE/DYNAMICWORLD/V1')
        .filter(col_filter)
        .select('label');

    return (ee.Image(DWcol.mode()).clip(geo))
}

